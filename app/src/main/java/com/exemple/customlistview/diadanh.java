package com.exemple.customlistview;

public class diadanh {
    public int id;
    public String ten;
    public String nghia;

    public diadanh(int id, String ten, String nghia) {
        this.id = id;
        this.ten = ten;
        this.nghia = nghia;
    }

    public int getId() {
        return id;
    }

    public String getTen() {
        return ten;
    }

    public String getNghia() {
        return nghia;
    }
}
